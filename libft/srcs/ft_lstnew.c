#include "libft.h"

t_list	*ft_lstnew(int value)
{
	t_list	*temp;

	temp = malloc(sizeof(t_list));
	if (!temp)
		return (NULL);
	temp->value = value;
	temp->next = NULL;
	return (temp);
}
